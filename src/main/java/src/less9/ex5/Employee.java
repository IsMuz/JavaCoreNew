package src.less9.ex5;

public class Employee {

    private long l;
    private String name;

    public Employee(long l, String name) {
        this.l = l;
        this.name = name;
    }

    public long getL() {
        return l;
    }

    public void setL(long l) {
        this.l = l;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
